package com.itheima_02;

public class IfDemo3 {
	public static void main(String[] args) {
		System.out.println("start");
		
		boolean b = true;
		
		if(b) {
			System.out.println("a");
		}else if(!b) {
			System.out.println("b");
		}else {
			System.out.println("c");
		}
		
		System.out.println("end");
	}
}    
